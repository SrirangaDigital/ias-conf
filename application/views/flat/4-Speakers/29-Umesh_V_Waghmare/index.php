            <p class="talk">Electrons in a crystal: Localized versus extended orbitals</p>
            <p>
            <span class="abstract">Abstract</span><br />
                In a crystal, an electron with a definite energy behaves like a wave that extends spatially through the crystal, and is described by an extended <i>Bloch</i> wave-function. Interestingly, the electronic structure of a crystal exhibits a “near-sightedness” [1], which is conceptually underlying the nature of chemical bonding in it. Its precise and local picture is obtained from the construct of Wannier functions, the localized orbitals [2] that form the basis for development of (a) simple models to capture the essential physics, differentiating between metals and insulators, (b) linearly scaling efficient computational methods to determine electronic structure [1], and (c) a picture of chemical bonding [3]. These ideas apply quite well to dynamical structure of a crystal, as described by the dispersion of its phonons or vibrational waves [4]. The speaker’s group have shown an interesting relationship between Wannier functions and non-Abelian Berry phases of Bloch electrons [5], which is centrally relevant to the nontrivial electronic topology exhibited by topological insulators, Weyl and Dirac semimetals [6]. 
            </p>
