            <p class="talk">Mycobacteria scapegoat host epigenetic factors and non-coding RNAs to sneak through host immune responses</p>
            <p>
            <span class="abstract">Abstract</span><br />
                Infections with pathogenic mycobacteria are a global concern, with tuberculosis (TB), caused by <i>Mycobacterium tuberculosis</i> (Mtb), itself afflicting about one-third of the world population. The success of the pathogen is due in part to its worthwhile co-evolution with the host which allows it to flourish amidst a focused immune response. Alongside being armed for self-defense, Mtb has also prudently garnered an armamentarium of mechanisms to modulate host cellular machinery to its favour. Such strategies include generation of regulatory T cells, compromising of host cell apoptosis, skewing of cytokine response, inhibition of autophagy, generation of foamy macrophages etc. Among the plethora of host molecules being hijacked by Mtb to furnish the immune evasion strategies; fine tuning of host epigenetic modifiers and non-coding RNAs like miRNAs serve as yet another line of ammunition in its arsenal. In this context, the present talk will focus on the ability of Mtb to strategize host miRNAs for inhibition of autophagy and to plant host epigenetic modifiers for generation of foamy macrophages, so as to create a haven in the hostile environment offered by the host. 
