            <p class="color-event text-bold">Inaugural Lecture</p>
            <p class="talk">Intriguing Structure and Transport Behaviour of Ultrathin Single Crystalline Gold Nanowires</p>
            <p>
            <span class="abstract">Abstract</span><br />
                Growing anisotropic nanostructures of high-symmetry materials is a challenging task owing to the symmetry-breaking that is required. We have developed a simple wet chemical method by which ultrathin (&lt; 2 nm diameter) Au nanowires could be grown in the solution phase. In the first part of the talk, the details of the mechanism of these wires using electron microscopy as the principal supporting tool will be discussed.
            </p>
            <p>        
                In the second part, methods to grow these wires on substrates and directly between pre-patterned contact pads will be discussed. This enables the direct measurement of electrical transport through these wires for the first time. An unexpected insulating behaviour in these wires with the resistivity decreasing as a function of the temperature of measurement was found. The implications of this observation will be discussed. 
            </p>
            <p>    
                In the third part, some intriguing results on the atomic structure of these nanowires where we find modulations of atomic planes normal to the growth directions  will be presented. 
            </p>       
            <p>
                Finally, some interesting applications of these ultrathin metal nanowires in sensing and catalysis will be presented.
            </p>
