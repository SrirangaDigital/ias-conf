            <p class="talk">Re-Os geochronology: Clues for past marine and atmospheric conditions</p>
            <p>
            <span class="abstract">Abstract</span><br />
                Changes in the seawater chemistry have influenced the biosphere greatly in the past. These important variations in past oceanic conditions and their precise timing can be constrained using Re-Os isotopic compositions of organic-rich marine sedimentary rocks (e.g. black shale). Re and Os present in black shales are mostly hydrogenous in nature and abundances of these elements, owing to their redox-sensitive nature, serve as a good measure for past ocean redox state. The <sup>187</sup>Re-<sup>187</sup>Os radioactive decay scheme in black shale yields its depositional age and the initial <sup>187</sup>Os/<sup>188</sup>Os ratio (Os<sub>i</sub>) of the sedimentary basin during the shale deposition period. Compilation of Os<sub>i</sub> ratios for marine shales of Proterozoic age shows a coarse synchrony with the atmospheric oxygen level, pointing to dominant role of oxidative weathering on past ocean 
                <sup>187</sup>Os/<sup>188</sup>Os ratio. The presentation will highlight some of these important research outcomes, based on Re-Os isotopic studies, on paleo-ocean and atmospheric conditions. 
