            <p class="color-event text-bold">Symposium in honour of Prof. Walter Kohn</p>
            <p class="talk">Beyond the conventional DFT: Life with “U”</p>
            <p>
            <span class="abstract">Abstract</span><br />
                While the conventional density functional theory (DFT) within the framework of local density approximation (LDA), or in its other flavor, generalized gradient approximation (GGA), has been surprisingly successful in predicting the properties of most materials, it fails in cases. Examples include <i>d</i>-electron-based transition metal oxides, or <i>f</i>-electron-based rare-earth compounds, materials for which the Coulomb correlation, namely Hubbard “U”, becomes important. The predictions of conventional DFT in these cases may turn out to be quantitatively wrong. We will discuss the recent development in handling such cases, thereby expanding the scope of DFT. The primarily focus of this talk is on two major developments, DFT+U and DFT+dynamical mean field theory (DMFT). The effectiveness of the methods will be demonstrated through applications in describing charge-ordered insulators and correlated metals.
