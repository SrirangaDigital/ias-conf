            <p class="talk">Experimental ecology and evolution in the laboratory</p>
            <p>
            <span class="abstract">Abstract</span><br />
                It is often believed the ecological and evolutionary timescales are very different
                and that it is difficult to study ecological and evolutionary processes together in
                a rigorous and controlled manner. However, as the experience of the last few decades
                shows, experimental ecology and evolution studies in laboratory settings provide us
                with the ability to rigorously examine such phenomena and test hypotheses about
                evolutionary and ecological mechanisms, and their interactions, in a manner that is
                not possible in the wild. In this talk, a few examples from work in
                the speakerâ€™s  laboratory over the past twenty years will be used to illustrate the power of this approach,
                and underscore the point that the evolutionary process is far more subtle and
                responsive to minor changes in ecology than is often thought to be the case.
