            <p class="talk">Challenges in the control of Kala-azar, a disease of poverty</p>
            <p>
            <span class="abstract">Abstract</span><br />
                Kala-azar (KA), a phlebotomine-borne neglected tropical and potentially fatal disease, is caused by parasites of <i>Leishmania donovani</i> complex. The <i>L. donovani</i> infection is limited to the Indian subcontinent and East Africa, where transmission is anthroponotic and continues to cause epidemics periodically. Control measures for KA are heavily dependent on chemotherapy. The specific drugs that work for KA treatment are limited in number, too expensive for most patients to afford and are associated with increasing parasite drug resistance. By and large, a small proportion of <i>L. donovani</i> infection manifests as a clinical disease while the majority of the infected individuals remain asymptomatic and thus contribute to the dissemination of this disease. Further, some patients develop post-kala-azar dermal leishmaniasis (PKDL), and these also play an important role in VL transmission. These are the major challenges to WHO initiative for the eradication of this deadly disease by 2020. This has forced researchers to think about other control measures, in particular, the development and implementation of an effective vaccine as people cured of <i>Leishmania</i> infections develop lifelong immunity. Therefore, prevention of KA through prophylactic/therapeutic vaccination is feasible.
