            <p class="talk">Importance of the Vindhya Basin in the Prehistory of the Indian Subcontinent </p>
            <p>
            <span class="abstract">Abstract</span><br />
                The Indian subcontinent has witnessed two major phases of human expansion out of Africa during the Pleistocene (during the last 2 million years). These two major events have been termed Out of Africa I and Out of Africa II. The first expansion is associated with <i>Homo erectus</i> and the  second one is associated with  <i>Homo sapiens</i>. Archaeological evidence from across the region has been documented indicating the presence of early human settlements ranging in time from 2 Myr to 1.2 Myr (lower Pleistocene) and late Pleistocene (80–100 Kyr) settlements of <i>Homo sapiens</i>. While the well-dated sites are not many, given the size and range of geographical environments across the subcontinent, the archaeological evidence has, however, placed  the subcontinent at the forefront of ongoing global debates on peopling of the world. Global debate regarding this expansion event is focussing on the timing of exit out of Africa and the routes of dispersal. The subcontinent has been placed at the crossroads of human expansion between Africa and Australia. The latter region was first colonized around 50–60 Kyr. Hence, not only the timing of <i>Homo sapiens</i> expansion into the subcontinent but also the intra-subcontinental expansion needs to be placed in geo-environmental perspective to be able to arrive at definite picture of the role of regional geographical environments within the subcontinent that may have played a pivotal in the expansion process within and without the subcontinent.
            </p>
            <p>        
                Based on the nature and geological context of archaeological (Palaeolithic) evidence in the subcontinent, a basin model has been developed to explain the settlement patterns associated with the both the expansion events. This model has identified a series of geological basins across the subcontinent that could be identified as core areas of Palaeolithic human settlements; these core areas are coterminous with the seven major Proterozoic Purana Basins and the interconnected Gondwana basins across the peninsular region. Among them the Vindhya Basin preserves the highest density of Palaeolithic settlements that have also preserved evidence for continuity of human occupation throughout the Pleistocene. This presentation focusses on the nature of evidence from this region and also from the related regions to emphasise  the need for multidisciplinary investigation at select sites while highlighting the achievements of Palaeolithic research and our understanding of cultural evolution during the Pleistocene.  
            </p>
