            <p class="color-event text-bold gap-above-med">Inaugural Lecture</p>
            <p class="talk">Transglycosylation by bacterial chitinases</p>
            <p>
            <span class="abstract">Abstract</span><br />
                Plants interact with a wide range of pathogens and have evolved mechanisms to recognize
                pathogen-derived molecules to elicit induced resistance. Unlike vertebrate animals, plants
                rely solely on innate immunity to ward off pathogenicmicrobes. The plants are able to sense
                evolutionarily conserved general elicitors of pathogens called pathogen-associated molecular
                patterns, and activate immune responses, a process that is known as pathogen-triggered
                immunity. Chitooligosaccharides (COS), released during plant fungal interaction, elicit
                plant defense upon recognition. In this paper, the progress made towards understanding the
                mechanism of COS sensing in plants and on the application of chitin/chitosan/COS in disease
                management would be focused. As the plants are able to recognize the chitin/chitosan
                oligomers and activate defense responses, there has been an increased interest in the use of
                chitin and chitosan in agriculture. Transglycosylation (TG) of short chain COS resulting
                in long chain COS, with the use of chitinases, holds promise. To realize such a potential,
                sources of chitinases have been identified in our lab. The topic would cover the efforts made
                in our lab to understand/improve TG by selected bacterial chitinase(s) with an interest to
                generate longer chain COS.
            </p>
