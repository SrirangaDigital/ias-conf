            <p class="talk">The Microbiome in Skin Health and Disease</p>
            <p>
            <span class="abstract">Abstract</span><br />
                The skin is the largest organ of the human body. The composition and abundance of the skin commensal microbiome have direct implications in the maintenance of skin health.  Healthy skin microbiome is not same for all. Dysbiosis (microbiome imbalance) on the skin is associated with chronic skin diseases. Past studies did not consider Asian/Indian individuals, which is important since these populations have diverse population histories. The speaker’s team has characterised the healthy facial skin microbiome in an Indian population of Dravidian descent and derived evidence that inter-individual variability in microbiome profile is significantly associated with sebum and hydration levels in specific facial regions. The skin microbiome was found to be temporally stable even after one year. Microbiome profile on the Indian skin is considerably different from that of the Caucasian skin, but is less dissimilar from that of the Chinese skin. These findings are important in understanding causes and differences in prevalence of various skin conditions like acne, psoriasis, dermatitis, etc.
